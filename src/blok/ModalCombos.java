    private Object [] preencheComboCenarios(){
         
         ArrayList<Object> cenarios = new ArrayList<>();
         cenarios.add(new String("Selecione"));
         for (IPlugin plugin : ((PluginController)this.controller).getPluginsCarregados()) {
            if(plugin instanceof ICenario){
                cenarios.add(plugin);
            }
        }
        
        return cenarios.toArray();
    }
    
    private Object [] preencheComboJogadores(){
         
         ArrayList<Object> jogadores = new ArrayList<>();
         jogadores.add(new String("Selecione"));
         for (IPlugin plugin : ((PluginController)this.controller).getPluginsCarregados()) {
            if(plugin instanceof IJogador){
                jogadores.add(plugin);
            }
        }
        
        return jogadores.toArray();
    }
    
    private void iniciar(){
        mainWindow.start(cenario, jogador);
        mainWindow.setVisible(true);
        this.setVisible(false);
    }

