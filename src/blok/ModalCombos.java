    private void btnComecarJogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComecarJogoActionPerformed
        if((!comboSelectCenario.getSelectedItem().equals("Selecione")) &&
           (!comboSelectJogador.getSelectedItem().equals("Selecione")) )
            this.iniciar();
        
        if (comboSelectCenario.getSelectedItem().equals("Selecione"))
            lblMsgSelecione.setText("Favor selecionar um Plugin de Cenário!");
        
        if(comboSelectJogador.getSelectedItem().equals("Selecione"))
            lblMsgSelecione.setText("Favor selecionar um Plugin de Jogador!");
    }//GEN-LAST:event_btnComecarJogoActionPerformed

